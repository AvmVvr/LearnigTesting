package com.datatype;

public class PrimitiveDataType {

//	Primitive  : - 
//	Byte //byte a = 5;
//	Short //short a =5;
//	Int // int a = 5;
//	Long //long a = 5l;
//	Float // float a = 5.0f;
//	Double // double a = 5.0;
//	Char //char a = 'A';(Alphabets)
//	Boolean //boolean a = true; (false) 

	public static void main(String[] args) {

		System.out.println("Primitive Datatype");
		System.out.println("________________________");
		System.out.println("Byte Information");
		System.out.println("Byte Memory Size: " + Byte.SIZE);
		System.out.println("Byte Minimum Value: " + Byte.MIN_VALUE);
		System.out.println("Byte Maximum Value: " + Byte.MAX_VALUE);

		System.out.println("________________________");
		System.out.println("Short Information");
		System.out.println("Short Memory Size: " + Short.SIZE);
		System.out.println("Short Minimum Value: " + Short.MIN_VALUE);
		System.out.println("Short Maximum Value: " + Short.MAX_VALUE);

		System.out.println("________________________");
		System.out.println("Float Information");
		System.out.println("Float Memory Size: " + Float.SIZE);
		System.out.println("Float Minimum Value: " + Float.MIN_VALUE);
		System.out.println("Float Maximum Value: " + Float.MAX_VALUE);
	}
}