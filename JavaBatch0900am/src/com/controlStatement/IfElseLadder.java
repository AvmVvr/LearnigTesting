package com.controlStatement;

public class IfElseLadder {
	public static void main(String[] args) {

		int a = 10;
		int b = 10;

		if (a < b) {
			System.out.println("a is small");
		}
		else if (a==b) {
				System.out.println("a is equal to b");			
		} else {
				System.out.println("a is big");
		}
	}
}
