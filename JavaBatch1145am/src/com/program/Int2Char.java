package com.program;

public class Int2Char {
	public static void main(String[] args) {
		int total = 1200;
		int myMark = 900;
		
		float average= (float)myMark/total;
		System.out.println(average);
		System.out.println("***********");
		
		
		
		float b = 5.25f;
		int a =(int) b;
		System.out.println(a);//5
		System.out.println(b);//5.0
	}

}
